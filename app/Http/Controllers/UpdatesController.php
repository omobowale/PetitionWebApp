    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'petition_id' => 'required|numeric',
            'update_title' => 'required|string|min:2',
            'update_details' => 'required|string|min:10',
        ]);

        $update = new Update();
        $update->petition_id = $request->input('petition_id');
        $update->updates_title = $request->input('update_title');
        $update->updates_details = $request->input('update_details');
        $update->save();

        return back()->with('message', 'You have successfully added an update');
    }

